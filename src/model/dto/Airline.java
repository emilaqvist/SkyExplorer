package model.dto;



/**
 * Represents an airline company.
 * This class is typically used in flight-related applications to store and retrieve airline details.
 * @author Mahyar
 */
public class Airline {
    private String name;

    /**
     * Retrieves the name of the airline.
     *
     * @return The airline name as a {@link String}.
     */
    public String getName() {
        return name;
    }
}